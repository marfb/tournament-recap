/**
 * Evaluates if given value is of type string
 * @param {any} value
 * @returns {boolean}
 */
export const validateString = (value) => typeof value === 'string';

/**
 * Evaluates if given value is of type boolean
 * @param {any} value
 * @returns {boolean}
 */
export const validateBoolean = (value) => typeof value === 'boolean';
